    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        
        
        // Hämta alla produkt id från försäljning
        // hämta alla antal från försäljning
        String getProduktID = "SELECT produktid FROM försäljning";
        
        ArrayList<HashMap<String, String>> produktids = new ArrayList<>();
        
        try{
            produktids = idb.fetchRows(getProduktID);
        } catch (InfException e){
            lblFelmed.setText("Problem med att hämta produkters id.");
        }
        
        // skapa en hasmap med produkt namn och antal
        HashMap<String, ArrayList<String>> produktAntalPris = new HashMap<>();
        
        for (HashMap<String,String> row : produktids){
            String produktid = row.get("ProduktID");
            
            String getAntal = "SELECT antal FROM försäljning WHERE produktid = '" + produktid + "'";
            String dbAntal = null;
            try{
                dbAntal = idb.fetchSingle(getAntal);
            } catch (InfException e){
                lblFelmed.setText("Problem med att hämta antal för " + produktid + " från databasen.");
            }
            
            // hämta produktens namn
            String getNamn = "SELECT namn FROM produkt WHERE produktid = '" + produktid + "'";
            
            String dbNamn = null;
            
            try{
                dbNamn = idb.fetchSingle(getNamn);
            } catch (InfException e){
                lblFelmed.setText("Problem med att hämta namnet för " + produktid + ".");
            }
            
            
            // Hämta priset för produkter
            String getPris = "SELECT pris FROM produkt WHERE produktid = '" + produktid + "'";
            
            String dbPris = null;
            
            try{
                dbPris = idb.fetchSingle(getPris);
            } catch (InfException e){
                lblFelmed.setText("Problem med att hämta priset för " + produktid + ".");
            }
            
            // Lägg in allt i hashmappen
            ArrayList<String> antalPris = new ArrayList<>();
            
            antalPris.add(dbAntal);
            antalPris.add(dbPris);
            
            produktAntalPris.put(dbNamn, antalPris);

            
            
        }
        
        // lägg in allt i table
        for (Map.Entry<String, ArrayList<String>> entry : produktAntalPris.entrySet()){
            String produkt = entry.getKey();
            ArrayList<String> antalPris = entry.getValue();
            
            double d_antal = Double.parseDouble(antalPris.get(0));
            double d_pris = Double.parseDouble(antalPris.get(1));
            
            double summa = d_antal * d_pris;
            
            
            ((DefaultTableModel) tblStatistik.getModel()).addRow(new Object[]{produkt, d_antal, summa});
            
            
        }
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

