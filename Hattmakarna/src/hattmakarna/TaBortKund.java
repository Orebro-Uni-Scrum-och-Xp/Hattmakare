    private void btnTaBortKundActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaBortKundActionPerformed
    String Email = tfEmail.getText(); //hämtar email som användare skrivit i tf
    
    if (Email.isEmpty()){
        JOptionPane.showMessageDialog(this, "Fyll i en Email.");
        return; //kontroll om fält är tomt, isf visas felmeddelande
    }
    
    try {
        String KundID = idb.fetchSingle("SELECT KundID FROM Kund WHERE Email = '" + Email + "'"); //hämtar kundid från databas baserat på angiven epost
        
    if (KundID == null){
        JOptionPane.showMessageDialog(this, "Ingen kund hittades med den e-postadressen.");
        return; //kontroll om det ej finns kund med angiven epost, isf visas felmeddelande
    }
    
    
    int confirm = JOptionPane.showConfirmDialog(this, "Är du säker på att du vill ta bort denna kund?", "Bekräfta", JOptionPane.YES_NO_OPTION); //visar ruta för att kontrollera att användare vill ta bort kund
    if (confirm == JOptionPane.YES_OPTION) { //kontroll om användare klicka ja 
        idb.delete("DELETE FROM Kund WHERE KundID = " + KundID); //tar bort kund från databas med matchande kundID
        JOptionPane.showMessageDialog(this, "Kunden har tagits bort"); //meddelande visas om att kund är borttagen
    }
    }
    
    catch (InfException e) {
        JOptionPane.showMessageDialog(this, "Ett fel inträffade: " + e.getMessage());
    }
    }//GEN-LAST:event_btnTaBortKundActionPerformed

